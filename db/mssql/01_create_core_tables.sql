/* ---------- 1. USERS ---------- */
CREATE TABLE dbo.USERS
(
    ID                  UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_USERS PRIMARY KEY DEFAULT NEWID(),
    EMAIL               NVARCHAR(256)    NOT NULL,
    PASSWORD_HASH       NVARCHAR(500)    NOT NULL,
    FULL_NAME           NVARCHAR(200)    NOT NULL,
    PHONE               NVARCHAR(20)     NULL,
    ROLE                VARCHAR(20)      NOT NULL CONSTRAINT DF_USERS_ROLE DEFAULT 'Customer',
    IS_EMAIL_VERIFIED   BIT              NOT NULL CONSTRAINT DF_USERS_IS_EMAIL_VERIFIED DEFAULT 0,
    EMAIL_VERIFY_TOKEN  NVARCHAR(200)    NULL,
    EMAIL_VERIFY_EXPIRY DATETIME2        NULL,
    IS_ACTIVE           BIT              NOT NULL CONSTRAINT DF_USERS_IS_ACTIVE DEFAULT 1,
    LOYALTY_POINT       INT              NOT NULL CONSTRAINT DF_USERS_LOYALTY_POINT DEFAULT 0,
    LAST_LOGIN_AT       DATETIME2        NULL,
    CREATED_AT          DATETIME2        NOT NULL CONSTRAINT DF_USERS_CREATED_AT DEFAULT SYSUTCDATETIME(),
    CREATED_BY          UNIQUEIDENTIFIER NULL,
    UPDATED_AT          DATETIME2        NULL,
    UPDATED_BY          UNIQUEIDENTIFIER NULL,
    CONSTRAINT UQ_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CK_USERS_ROLE CHECK (ROLE IN ('Customer','Staff','Admin'))
);
GO

