    CONSTRAINT CK_INVENTORIES_ON_HAND CHECK (ON_HAND >= 0),
    CONSTRAINT CK_INVENTORIES_RESERVED CHECK (RESERVED >= 0 AND RESERVED <= ON_HAND)
