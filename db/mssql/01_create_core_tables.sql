/* ---------- 15. ORDER_AUDIT_LOGS (BR-14) ----------
   This table IS the audit trail; CHANGED_BY / CHANGED_AT capture who/when. */
CREATE TABLE dbo.ORDER_AUDIT_LOGS
(
    ID          UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_ORDER_AUDIT_LOGS PRIMARY KEY DEFAULT NEWID(),
    ORDER_ID    UNIQUEIDENTIFIER NOT NULL,
    FROM_STATUS VARCHAR(20)      NULL,
    TO_STATUS   VARCHAR(20)      NOT NULL,
    CHANGED_BY  UNIQUEIDENTIFIER NULL,   -- NULL = system
    REASON      NVARCHAR(500)    NULL,
    CHANGED_AT  DATETIME2        NOT NULL CONSTRAINT DF_ORDER_AUDIT_LOGS_CHANGED_AT DEFAULT SYSUTCDATETIME(),
    CONSTRAINT FK_ORDER_AUDIT_LOGS_ORDERS FOREIGN KEY (ORDER_ID) REFERENCES dbo.ORDERS(ID) ON DELETE CASCADE,
    CONSTRAINT FK_ORDER_AUDIT_LOGS_USERS FOREIGN KEY (CHANGED_BY) REFERENCES dbo.USERS(ID)
);
GO

